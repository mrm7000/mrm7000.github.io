    print(value)
end)
Folder:Dropdown("Example Dropdown",{"A","B","C"},function(option)
    print(option)
end)
Folder:Box("Example Box Number","number",function(value)
    print(value)
end)
Folder:Box("Example Box String","string",function(value)
    print(value)
end)
Folder:DestroyGUI()
Folder:GuiSettings()
