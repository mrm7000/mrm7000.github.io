local Library = loadstring(game:HttpGetAsync'https://mrm7000.github.io/wally%20ui%20v3/library.lua')()
local Window = Library:CreateWindow("Example")
local Folder = Window:CreateFolder("Example Folder")
Folder:Label("Example Label",Color3.fromRGB(38,38,38),Color3.fromRGB(0,216,111))
Folder:Button("Example Button", function()
    print("Works!")
end)
Folder:Toggle("Example Toggle", function(bool)
   shared.Toggle = bool
   print(shared.Toggle) 
end)
